"""创作偏好档案相关 Prompt。

档案是贯穿全书的创作宪法(文风/禁忌/读者定位/其他主张),注入所有生成环节。
两个 prompt:
- PROFILE_ABSORB_PROMPT:把研讨对话里聊出的新主张合并进已有档案。
- PROFILE_EXTRACT_PROMPT:对已生成的书,从概念/架构/简介/抽样正文反向提炼出档案,
  让老书不用作者手填就有一份与正文相符的档案。
"""

PROFILE_ABSORB_PROMPT = """\
你是小说创作顾问。作者刚在和 AI 编辑的对话里表达了一些创作主张,现在要把这些主张
沉淀进这本书的「创作偏好档案」(它会贯穿后续所有章节的生成/重写/润色)。请把新主张
合并进已有档案。

【当前创作偏好档案(可能为空)】
- 文风:{style}
- 禁忌/避雷:{taboos}
- 读者定位:{audience}
- 其他创作主张:{other}

【本次对话提炼出的新主张】
{directive}

合并要求:
1. 把新主张按性质归入对应字段:文字风格/叙事腔调/句式 → 文风;不要出现的内容/雷区/避雷
   → 禁忌/避雷;目标读者/受众/年龄段 → 读者定位;其余创作原则 → 其他创作主张。
2. 与已有内容合并,去重、化解矛盾(新主张优先于旧档案),语言精炼,同字段多条用分号分隔。
3. 只吸收对话里真实表达或明确暗示的主张,不要凭空发明。某字段没有内容就留空字符串。
4. 严格按下面 JSON 输出,不要输出任何额外文字、解释或代码块标记:
{{"style": "...", "taboos": "...", "audience": "...", "other": "..."}}"""

PROFILE_EXTRACT_PROMPT = """\
你是资深小说编辑。下面是一部小说目前已有的内容(概念、架构、简介,以及若干章节正文节选)。
请从中反向提炼出这本书的「创作偏好档案」,作为后续生成/重写/润色要遵循的创作宪法。

【小说现有内容】
{context}

提炼要求:
1. 文风:从正文节选归纳实际的叙事腔调、句式节奏、修辞偏好(如"冷峻克制、短句白描、少形容词")。
2. 禁忌/避雷:从概念/架构/正文里明显被回避或确立为雷区的内容(没有明显线索就留空,不要硬编)。
3. 读者定位:从题材、节奏、概念推断目标读者(如"面向青少年的快节奏网文")。
4. 其他创作主张:贯穿全书的叙事原则、结构习惯(如"每章末留钩子""多线叙事")。
5. 只基于给定内容客观归纳这本书"实际是什么样",不要添加内容里没有的主张;某字段没把握就留空字符串。
6. 语言精炼,同字段多条用分号分隔。
7. 严格按下面 JSON 输出,不要输出任何额外文字、解释或代码块标记:
{{"style": "...", "taboos": "...", "audience": "...", "other": "..."}}"""
