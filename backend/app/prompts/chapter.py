# app/prompts/chapter.py
# -*- coding: utf-8 -*-
"""逐章生成 Prompt:滚动摘要 → 草稿 → 定稿(借鉴 AI_NovelGenerator 的多阶段思路)。

上下文组装(见 docs/02-data-model.md 数据流):
  本章蓝图 + 下章蓝图(承上启下)+ 最近章节正文尾部 + 滚动摘要
  + {style_directives} 倾向块
"""

# =============== 滚动摘要:压缩"更早"的剧情 ===============
ROLLING_SUMMARY_PROMPT = """\
你是专业小说编辑。请把以下已完成章节的剧情压缩成"前情摘要",供后续写作参考。

已有前情摘要(可能为空):
{previous_summary}

新完成的章节正文:
第{chapter_number}章《{chapter_title}》:
{chapter_text}

要求:
1. 把新章节的关键剧情融入前情摘要,输出合并后的完整摘要
2. 保留:关键事件、人物状态变化、新揭示的信息、未解决的悬念
3. 删除:场景细节、对话原文、修辞
4. 按时间顺序组织,总长不超过 800 字

仅输出合并后的前情摘要,不要解释。
"""

# =============== 章节草稿 ===============
CHAPTER_DRAFT_PROMPT = """\
你是一位职业小说家,正在创作长篇小说。请写出第{chapter_number}章的完整正文。

【小说架构(节选)】
{architecture_brief}

【前情摘要】
{rolling_summary}

【最近章节结尾(直接上文,衔接必须自然)】
{recent_tail}

【人物当前状态(硬约束,必须遵守,不得违反)】
{hard_constraints}

【伏笔回收提醒(到期伏笔应在本章或近期自然回收)】
{foreshadow_reminders}

{avoid_repetition}
{revision_block}
【本章蓝图】
第{chapter_number}章《{chapter_title}》
- 本章定位:{chapter_role}
- 核心作用:{chapter_purpose}
- 悬念密度:{suspense_level}
- 伏笔操作:{foreshadowing}
- 涉及人物:{characters_involved}
- 关键道具:{key_items}
- 场景地点:{scene_location}
- 本章简述:{chapter_summary}

【下一章蓝图(为其留好铺垫,不要写进本章)】
{next_chapter_brief}

{style_directives}
【反 AI 腔约束(务必遵守)】
1. 禁神态套话:"眼中闪过一丝""嘴角勾起""微微上扬""勾起一抹弧度""邪魅一笑""面色铁青""眸光一沉""眼底闪过""不禁""不由得""下意识地""空气仿佛凝固""时间仿佛静止""命运的齿轮开始转动"
2. 禁稳妥套话堆砌:"沉默片刻""微微一笑""轻声说道""缓缓开口""淡淡地道""心中暗道"全章各最多出现 1 次
3. 禁总结说教:"综上所述""总而言之""总的来说""值得注意的是""由此可见""毫无疑问""这个故事告诉我们""可想而知";章末与段尾不得总结升华、点题,用场景或动作收束
4. 禁论文式连接:"首先/其次/再次/最后""一方面/另一方面""不仅……而且""虽然……但是"
5. 句式节奏:每 150 字至少出现 1 个不超过 6 字的短句;不得连续 3 句等长;长短句交错
6. 比喻限量:全章"仿佛/宛如/好似/恍若"类比喻不超过 3 处,能白描就白描
7. Show don't tell:禁"他感到无比绝望""心中五味杂陈"式情绪形容词直评,情绪用动作、环境、潜台词呈现

写作要求:
1. 目标字数:约{word_number}字(上下浮动两成内为宜;不要为凑字数注水拖沓,也不要为压字数硬砍情节)
2. 节奏控制:本章分{scene_count}个场景段落,每段约{scene_words}字,场景间用空行自然过渡
3. 严格落实本章蓝图的定位、伏笔操作与简述,不得偏离主线
4. 与"最近章节结尾"无缝衔接,开头不要重复前文,不要写"上回说到"
5. 人物言行必须符合已建立的性格与状态,不得与前情矛盾
6. 章末留钩子,呼应下一章蓝图
7. 只写正文,不要章节标题、序号、任何解释或元信息

现在开始写第{chapter_number}章正文:
"""

# =============== 定稿:自查+润修 ===============
CHAPTER_FINALIZE_PROMPT = """\
你是资深文学编辑。以下是长篇小说第{chapter_number}章《{chapter_title}》的草稿,请修订出定稿。

【本章蓝图(修订不得偏离)】
- 核心作用:{chapter_purpose}
- 伏笔操作:{foreshadowing}
- 本章简述:{chapter_summary}

【前情摘要(检查一致性用)】
{rolling_summary}

【草稿正文】
{draft_text}

{style_directives}
修订要求:
1. 修正:与前情矛盾之处、人物言行失当、逻辑漏洞、时间线错误
2. 提升:删除冗余重复,收紧节奏,增强场景感与对话的自然度
3. 保持:剧情走向、伏笔操作、章末钩子不变;字数与草稿相当
4. 只输出修订后的完整正文,不要标注修改点,不要任何解释

修订后的第{chapter_number}章正文:
"""

# =============== 字数守卫:压缩重写(中度超标) ===============
COMPRESS_REWRITE_PROMPT = """\
你是资深文学编辑。以下章节正文约{actual_words}字,超出目标字数({target_words}字)。
请在保留全部核心情节、人物互动、伏笔操作的前提下,将正文精简到{target_words}字左右。

精简策略:
1. 删除重复描写、过度渲染的环境/心理段落
2. 合并功能相同的对话轮次(同一信息点只保留最有力的一次表达)
3. 缩减过渡性叙述,保留场景转换的关键动作
4. 不得删除:伏笔埋设/回收、关键对话、章末钩子、人物状态变化、情节转折

【本章蓝图(不可偏离)】
{chapter_summary}

【原文】
{draft_text}

输出精简后的完整正文(约{target_words}字),不要解释,不要标注:
"""

# =============== 字数守卫:拆章断点选择(严重超标) ===============
SPLIT_POINT_PROMPT = """\
你是小说编辑。以下章节正文约{actual_words}字,需要拆成两章(每章约{target_words}字)。

请找出一个最佳拆分点,要求:
1. 必须是自然的叙事断点:场景转换、时间跳跃、视角切换、或重大事件前后
2. 不得在对话中间、动作序列中间、或悬念揭示中间断开
3. 拆分后两章各自有完整的起承转合,前章末尾有钩子,后章开头能衔接
4. 尽量让两章字数接近均分

【原文(段落以空行分隔)】
{full_text}

请严格按以下 JSON 格式输出(不要输出其他任何内容):
{{"split_paragraph_index": <断开处之前最后一段的段落序号,从0开始>, "reason": "<为什么在这里断开的一句话说明>", "chapter_a_title": "<前半章的标题,10字以内>", "chapter_b_title": "<后半章的标题,10字以内>", "chapter_a_summary": "<前半章的一句话概要>", "chapter_b_summary": "<后半章的一句话概要>", "foreshadowing_goes_to": "<a或b,本章大纲中的伏笔操作归哪半章>"}}
"""

# =============== 重写研讨(对话式:聊清"到底哪里不满意" → 蒸馏成重写要求)===============
# 用户点"重写"时往往只有一句模糊意见(如"节奏太拖"),说不清具体想要什么。
# 这套对话先把真实意图聊清楚,再蒸馏成一条精准的重写要求注入草稿,提升重写命中率。
# 与架构研讨(snowflake.ARCH_CHAT_SYSTEM_PROMPT)同构,只是上下文换成单章蓝图+正文。
REVISE_CHAT_SYSTEM_PROMPT = """\
你是一位资深小说编辑,正在和作者一起打磨这一章。作者对当前 AI 生成的正文不太满意,
准备重写,你要通过对话帮作者把"到底哪里不对、重写时真正想要什么"聊清楚。

【本章蓝图(重写不得偏离的骨架)】
{blueprint_block}

【当前正文(作者不满意的这一版,节选)】
{chapter_block}

对话原则:
1. 先听懂作者的不满,顺着追问关键的一两点(如:是节奏拖沓?对话不像角色?情绪没到位?结尾仓促?某段太水?)。
2. 一次只聚焦一个最关键的问题,给 2-3 个具体的改法供作者选,而不是空泛地问"你想怎么改"。
3. 可以引用正文里的具体句子或段落来确认问题所在,让讨论落到实处。
4. 语气像并肩创作的伙伴,简洁、有具体建议,不要长篇大论、不要说教。
5. 你是在"聊清楚要怎么改",不是现在就重写正文;不要输出大段新正文,也不要输出 JSON。
6. 当作者的想法已经比较明确时,可以主动说一句"那我们就按这个方向重写这一章",提示作者去点开始重写。"""

# 把整段研讨对话蒸馏成"给重写的修改意见"(独立调用,不污染对话)。
# 蒸馏结果会回填进重写文本框,作为 generate_chapter 的 revision 参数注入草稿。
REVISE_DISTILL_PROMPT = """\
下面是作者与编辑关于"这一章该怎么改"的完整对话。请把对话中作者明确表达或倾向的
修改意见,提炼成一段"给重写的修改意见",供重写这一章时高优先级遵循。

【对话记录】
{transcript}

要求:
1. 只提炼对话里真实出现或明确暗示的意见,不要凭空发明新情节。
2. 用简洁的祈使句分条写(如"开头铺垫砍掉一半,直接进冲突""把主角的犹豫改成隐忍,别写哭")。
3. 只写"要怎么改"的要点,不要复述当前正文,不要解释理由。
4. 改写意见须在本章蓝图范围内,不要让作者推翻蓝图(如改主线走向)——那是改大纲的事。
5. 若对话尚未谈出任何明确修改意见,只输出一个短横线"-"。

直接输出修改意见正文(可分条),不要 JSON、不要额外说明。"""
