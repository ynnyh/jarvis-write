"""润色引擎 Prompt:锁情节事实 + 去 AI 味。

两条铁律:
1. 只改文笔,不改情节事实(锁定事实清单,润色后校验)
2. 默认去 AI 腔(排比堆砌/造句定式/滥用比喻词/金句癖/情绪直喊/空洞升华)
"""

# 去 AI 味的通用规则,始终注入(无论用户是否勾选"去AI味")
_DEAI_RULES = """\
【去除 AI 腔(务必遵守)】
- 删掉刻意的排比句堆砌和三连比喻(如"是A,是B,是C")
- 避免"不是……而是……""与其说……不如说……""仿佛/宛如……又像……"等 AI 高频造句定式
- 少用"仿佛""宛如""彷佛""好似"等比喻连接词,能直接白描就白描
- 不要每段都憋金句/警句/哲理升华,克制"点题"的冲动
- 情绪靠动作、细节、对话呈现,不要直接写"他感到无比绝望""心中五味杂陈"这类情绪标签
- 删除段末空洞的抽象总结和拔高陈词
- 保留人物特有的语气和口吻,不要把所有人都改成同一种"高级"腔调
"""

# =============== 整章/选段润色 ===============
POLISH_PROMPT = """\
你是资深文学编辑,请润色以下小说片段。这是纯文笔加工,不是改写剧情。

【必须锁定的情节事实(逐条保留,不得增删改)】
{locked_facts}

【待润色文本】
{text}

{style_directives}
{deai_rules}
润色铁律:
1. 情节、人物行为、对话内容的实质、时间地点因果 —— 一律不得改变
2. 不得新增原文没有的事件、人物、道具、设定
3. 不得删除任何情节事实(可以删冗余修饰,但不能删"发生了什么")
4. 只调整:遣词、句式、节奏、描写密度、语气
5. 保持与原文相当的篇幅(±15%)

只输出润色后的完整文本,不要标注改动,不要任何解释。
"""

# =============== 阅读中片段润色(带用户方向,单次调用) ===============
FRAGMENT_POLISH_PROMPT = """\
你是资深文学编辑,请润色下面这段小说正文。这是纯文笔加工,不是改写剧情。

【本章蓝图摘要(仅作上下文,润色范围仅限给定片段)】
{chapter_summary}

【用户润色方向】
{direction}

【待润色片段】
{fragment}

润色铁律:
1. 情节、事实、人物状态、对话实质、伏笔安排 —— 一律不得改变
2. 不得新增原文没有的事件、人物、道具、设定;不得删除任何情节事实
3. 只调整:遣词、句式、节奏、描写密度、语气
4. 保持与原文相当的篇幅(±15%)
5. 严格遵循用户润色方向;方向与铁律冲突时,以铁律为准

只输出润色后的片段文本,不要标注改动,不要任何解释。
"""

# =============== 润色前:抽取情节事实清单(锁定用) ===============
FACT_LOCK_PROMPT = """\
从以下小说片段中,抽取"润色时必须保留、不得改动"的情节事实清单。

【片段】
{text}

抽取要点:
- 谁做了什么(关键动作/事件)
- 谁说了什么(对话的核心信息,不是原句)
- 状态变化(受伤/获得/失去/移动/揭露)
- 关键道具、地点、时间

严格按 JSON 输出(不要 markdown 围栏,不要解释):
{{"facts": ["事实1", "事实2", ...]}}

只列"改了就算改剧情"的硬事实,不要列修辞和描写细节。最多 15 条。
"""

