# 主编评分:四维打分 + 短评 + 3 条可执行建议(必须引用原文举证)
REVIEW_PROMPT = """\
你是资深网文主编,审读一章正文并给出专业评价。

【本章蓝图】
{outline_block}

【正文】
{content}

从四个维度打分(1-10,整数,敢打低分,8 分以上必须名副其实):
- plot 情节:推进是否有效,冲突是否扎实,是否兑现了蓝图
- prose 文笔:语言质量,画面感,有没有 AI 腔和套话
- pacing 节奏:张弛是否得当,有没有拖沓或赶戏
- character 人物:言行是否符合人设,动机是否可信

给出最该改的建议(0-3 条,按严重程度排序)。若本章已足够好、没有值得动的硬问题,
就返回空数组——不要为了凑数硬挑毛病。每条建议必须:
1. evidence 引用正文中**逐字存在**的原句(15-40 字)作为证据——没有原文证据的问题不要提
2. issue 说清这句/这段暴露了什么问题
3. fix 给出具体可执行的改法(改写方向,不是空话)

只输出 JSON(suggestions 可为空数组):
{{
  "scores": {{"plot": 7, "prose": 6, "pacing": 7, "character": 8}},
  "comment": "两三句总评,一针见血",
  "suggestions": [
    {{"evidence": "正文原句", "issue": "这里暴露的问题", "fix": "具体改法"}}
  ]
}}
"""

# 校对:错别字/语病/标点/重复用词,输出可精确替换的问题清单
PROOFREAD_PROMPT = """\
你是出版社校对,逐句检查下面的正文,找出确定的文字问题。

【正文】
{content}

只找以下四类硬伤,宁缺毋滥(风格问题、主观优化不要报):
- typo 错别字/用字错误(如"她/他"混用、同音字错)
- grammar 语病(成分残缺、搭配不当、指代不明)
- punct 标点错误(引号不配对、句号逗号误用)
- dup 近距离重复用词(同一词在相邻句子里刺眼地重复)

要求:
1. original 必须是正文中**逐字存在**的连续片段(10-40 字,含足够上下文以便唯一定位)
2. suggestion 是把 original 原样替换后的正确版本(只改必要的字)
3. 最多报 20 处,按严重程度排序;没有问题就返回空数组

只输出 JSON:
{{
  "issues": [
    {{"type": "typo", "original": "他身量不高,眉眼却", "suggestion": "她身量不高,眉眼却", "reason": "人称错误,此处指的是苏小棠(女)"}}
  ]
}}
"""
